package _1.PastaDeEstudos;

public class Operacoes {
    public static void main(String[] args){
        //float para divisao com decimais
        float operacao = 10;
        float operacao2 = 20;

        //Soma
        System.out.println(operacao*operacao2);

        //Subtração
        System.out.println(operacao-operacao2);

        //Multiplicação
        System.out.println(operacao*operacao2);

        //Divisão
        System.out.print(operacao/operacao2);
    }
}
