package estudosantigos;

public class PadraoPessoa {
    private String nome;
    private int idade;

    //getNome = Definir um nome para a linha
    public String getNome() {
        return nome;
    }

    //setNome = salvar o nome para a linha "nome"
    public void setNome(String nome) {
        this.nome = nome;
    }

    //getIdade = Definir idade para a linha
    public int getIdade() {
        return idade;
    }

    //setIdade = salvar idade para a linha "idade"
    public void setIdade(int idade) {
        this.idade = idade;
    }
}
