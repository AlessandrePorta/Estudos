package estudosantigos;

public class OperadorDeAdicao {
    public static void main(String[] args) {
        int idade = 22;

        //Soma da idade
        idade += 1;
        System.out.println(idade);

        //Multiplicação da idade
        idade *= 2;
        System.out.println(idade);

        //Subtração da idade
        idade -= 2;
        System.out.println(idade);

        //Divisão da idade
        idade /= 2;
        System.out.println(idade);

        //Resto da idade
        idade %= 2;
        System.out.println(idade);

        //Resultados de acordo com a última impressão.
    }
}